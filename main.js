function hammingDistance(str1, str2) {
    var i, distance = 0;
    for (i = 0; i < str1.length; i += 1) {
        if (str1[i] !== str2[i]) {
            distance += 1;
        }
    }
    return distance;
}

function getClosestProfile(answerString) {
    var minDistance = Infinity;
    var closestProfile = null;
    var i, key, distance;

    for (i = 0; i < keys.length; i += 1) {
        key = keys[i];
        distance = hammingDistance(key, answerString);
        if (distance < minDistance) {
            minDistance = distance;
            closestProfile = experts[key];
        }
    }

    return {
        profileId: closestProfile,
        distance: minDistance
    };
}

function updateProgress() {
    var progress = (currentIndex / totalQuestions) * 100;
    progressBar.style.width = progress + '%';
}

function renderQuestion() {
    questionCounter.textContent = 'Вопрос ' + (currentIndex + 1) + ' из ' + totalQuestions;
    questionText.textContent = questions[currentIndex];
    door1.textContent = options1[currentIndex];
    door2.textContent = options2[currentIndex];
    updateProgress();
}

function showTest() {
    startScreen.classList.add('hidden');
    resultScreen.classList.add('hidden');
    testScreen.classList.remove('hidden');
    renderQuestion();
}

function showResult() {
    var result = getClosestProfile(answers);
    var profileData = profiles[result.profileId];

    testScreen.classList.add('hidden');
    resultScreen.classList.remove('hidden');
    progressBar.style.width = '100%';

    resultTitle.textContent = profileData.title;
    resultDescription.textContent = profileData.description;
    resultNote.textContent = 'Это предварительная рекомендация. В текущей версии алгоритм сравнивает ваши ответы с экспертными шаблонами и помогает понять, какой профиль вам ближе.';
}

function registerAnswer(value) {
    answers += value;
    currentIndex += 1;

    if (currentIndex >= totalQuestions) {
        showResult();
        return;
    }

    renderQuestion();
}

function resetTest() {
    answers = '';
    currentIndex = 0;
    showTest();
}

function scrollToTest(event) {
    if (event) {
        event.preventDefault();
    }
    document.getElementById('test').scrollIntoView({ behavior: 'smooth' });
}

function openProfileDialog(profileKey) {
    var data = profileDetails[profileKey];
    if (!data) {
        return;
    }
    dialogTag.textContent = data.tag;
    dialogTitle.textContent = data.title;
    dialogText.textContent = data.text;
    profileDialog.showModal();
}

